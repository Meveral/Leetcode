struct ListNode {
	int val;
	ListNode *next;
	ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
public:
	ListNode* reverseList(ListNode* head) {
		if (head == NULL)
			return NULL;

		ListNode* tmp = NULL;
		
		while (head != NULL)
		{
			ListNode* tmp_node = head->next;
			head->next = tmp;
			tmp = head;
			head = tmp_node;
		}
		return tmp;

	}
};

